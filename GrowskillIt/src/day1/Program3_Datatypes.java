package day1;

public class Program3_Datatypes {
	public static void main(String[] args) {
		//boolean takes 1 bit
		boolean result=false;
		System.out.println(result);
		
		//byte take 1 byte(8 bits)
		byte ans= -128;
			System.out.println(ans);
			
			//short take 2 bytes
			//-32000 to 32000
			short data=-32000;
			System.out.println(data);
			
			//char take 2 bytes
			char alpha='A';
			System.out.println(alpha);
			
			//long takes 8 bytes
			long phoneNumber=6306542345L;
			System.out.println(phoneNumber);
			//floating points--marks
			//float 4 bytes
			float marks1=9.12345678F;
			System.out.println(marks1);
			
			float marks2=91.2345678F;
			System.out.println(marks2);
			
			float marks3=912.345678F;
			System.out.println(marks3);
			
			float marks4=9123.45678F;
			System.out.println(marks4);
			
			//double-8 bytes
			double data1=9.123456789012345;
			System.out.println(data1);
			
			
			
			
			
	}

}
