package day1;

public class Program_Increment {
	public static void main(String[] args) {
		int num2=9;
		 
		 System.out.println(++num2);//first increment then value assign
		 //num2=10 becomes
		 System.out.println(num2);
		
		 int num3=10;
		 num3=num3+1;//incremented by  factor of 1
		 System.out.println(num3);
		 
		 //other way
		 int num=11;
		 num++;//incremented by  factor of 1
		 System.out.println(num);
		 System.out.println(++num);
		 
		 
		 
	}

}
